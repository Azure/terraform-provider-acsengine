package utils

// Bool returns pointer to bool input
func Bool(input bool) *bool {
	return &input
}

// Int32 returns pointer to int32 input
func Int32(input int32) *int32 {
	return &input
}

// Int64 returns pointer to int64 input
func Int64(input int64) *int64 {
	return &input
}

// String returns pointer to string input
func String(input string) *string {
	return &input
}
